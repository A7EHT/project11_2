def str_upper(text):
    """
    увеличивает текст
    :param text: некоторый текст
    :return: тот же текст но в верхнем регистре
    """
    return text.upper()


def str_title(text):
    """
    делает заглавной только первую букву в слове
    :param text: некоторый текст
    :return: во всем тексте все слова будут иметь первую букву с верхнего регистра
    """
    return text.title()

